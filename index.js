// Update language
function updateLanguage(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang]?.[key]) {
      el.textContent = translations[lang][key];
    }
  });
}

// Create feature card
function createCard({ icon, i18n_title, i18n_desc }, statusIcon = 'check-circle') {
  const lang = document.getElementById('languageSelect').value;
  const card = document.createElement('div');
  card.className = 'feature-card';
  const title = translations[lang][i18n_title];
  const desc = translations[lang][i18n_desc];

  card.innerHTML = `
    <div class="feature-header">
      <div class="feature-icons">
        <div class="feature-icon"><i data-lucide="${icon}"></i></div>
        <div class="status-icon"><i data-lucide="${statusIcon}"></i></div>
      </div>
      <h3 class="feature-title" data-i18n="${i18n_title}">${title}</h3>
    </div>
    <div class="feature-content">
      <p class="feature-description" data-i18n="${i18n_desc}">${desc}</p>
    </div>
  `;
  return card;
}

// Render feature sections
function renderSection(gridId, features, statusIcon) {
  const grid = document.getElementById(gridId);
  grid.innerHTML = '';
  features.forEach(feature => grid.appendChild(createCard(feature, statusIcon)));
}

